	public static final int maxZ2 = 18;
	public static final int difference = 6;
	
	public boolean generate(World world, Random rand, int x, int y, int z) {
		if (world.isRemote) {return false;}
		
		//Generate
		if (!WorldGen.isWHDRegionClear(world, x, y+1, z, maxX2, maxY2, maxZ2) || !BlockRainbowGrass.class.isAssignableFrom(world.getBlock(x+maxX/2,y,z+maxZ/2).getClass()) || !world.canBlockSeeTheSky(x+maxX/2,y+1,z+maxX/2)) {return false;}
		WorldGen.WHDRegionClear(world, x, y, z, maxX2, maxY2, maxZ2); //Safety precautions.
		WorldGen.roundedFlattenGround(world, x, y, z, maxX2, maxZ2, YetAnotherMod.rainbowStoneRefined, YetAnotherMod.rainbowStoneRefined);
		generateActual(world, rand, x+(difference/2), y+1, z+(difference/2));
		
		//Spawn Defenders
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+3, y+1, z+(maxZ2/2));}
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+maxX2-3, y+1, z+(maxZ2/2));}
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+(maxX2/2), y+1, z+3);}
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+(maxX2/2), y+1, z+maxZ2-3);}
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+3, y+1, z+maxZ2-3);}
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+maxX2-3, y+1, z+3);}
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+3, y+1, z+3);}
		if (rand.nextInt(2) == 0) {spawnGolem(world, rand, x+maxX2-3, y+1, z+maxZ2-3);}
		
		return true;
	}
	
	private static void spawnGolem(World world, Random rand, int x, int y, int z) {
		EntityRainbowGolem entitygolem = new EntityRainbowGolem(world);
        entitygolem.setLocationAndAngles((double)x + 0.5D, (double)y + 1.95D, (double)z + 0.5D, rand.nextFloat()*360, rand.nextFloat()*360);
        world.spawnEntityInWorld(entitygolem);
	}

